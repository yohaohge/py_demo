from collections import Iterator
from collections import Iterable

print(isinstance([], Iterable))
print(isinstance([], Iterator))

# 可以被next()函数调用并不断返回下一个值的对象称为迭代器：Iterator


for x in [1, 2, 3, 4, 5]:
    print(x)

# 首先获得Iterator对象:
it = iter([1, 2, 3, 4, 5])
# 循环:
while True:
    try:
        # 获得下一个值:
        x = next(it)
    except StopIteration:
        # 遇到StopIteration就退出循环
        break

print('next():')
it = iter([1, 2, 3, 4, 5])
print(next(it))
